package sept.sept9;

public class Do002 {
    public static void main(String[] args) {
        System.out.println("Start of the Loop");
        int a = 0;
        do{
            System.out.println("Value of a -> " + a);
            a--;
        }while(a>-5);
        System.out.println("End of the Loop");
    }
}
